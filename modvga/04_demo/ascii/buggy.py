from machine import Pin, SPI
from gd import *

# Initialize the SPI Bus (on Pyboard)
# Hardware SPI
spi = SPI(2) # MOSI=Y8, MISO=Y7, SCK=Y6, SS=Y5
spi.init( baudrate=2000000, phase=0, polarity=0 )
# We must manage the SS signal ourself
ss = Pin( Pin.board.Y5, Pin.OUT )


# Gameduino Lib
gd = Gameduino( spi, ss )
gd.begin()
gd.ascii(font_color=rgb(100,250,100), fill_char=ord('M') )

gd.putstr( 5 ,  0, "  *** DEMOs ***" )
gd.putstr( 5 ,  1, " 04_demo/ascii/buggy.py " )

# Drawing from the 4th ligne because of a bug
print( 'That''s all folks')
